export default function TechMarquee() {
    const [visible, setVisible] = useState(false)
    const [lastScrollY, setLastScrollY] = useState(0)

    useEffect(() => {
        const handleScroll = () => {
            const currentScrollY = window.scrollY

            if (currentScrollY < 50) {
                setVisible(false)
            } else if (currentScrollY > lastScrollY) {
                setVisible(true)
            } else {
                setVisible(false)
            }

            setLastScrollY(currentScrollY)
        }

        window.addEventListener("scroll", handleScroll, { passive: true })
        return () => window.removeEventListener("scroll", handleScroll)
    }, [lastScrollY])

    return (
        <div className={`fixed bottom-0 left-0 right-0 z-40 bg-background/60 backdrop-blur-md border-t border-border/30 overflow-hidden transition-all duration-300 ${visible ? "translate-y-0" : "translate-y-full"}`}>
            <div className="marquee-container">
                <div className="marquee-content">
                    {technologies.map((tech, index) => (
                        <div
                            key={`${tech.name}-${index}`}
                            className="flex items-center gap-2 text-muted-foreground/60 px-4"
                        >
                            <span className="flex-shrink-0">{tech.icon}</span>
                            <span className="text-xs font-medium tracking-wide">{tech.name}</span>
                        </div>
                    ))}
                </div>
                <div className="marquee-content" aria-hidden="true">
                    {technologies.map((tech, index) => (
                        <div
                            key={`${tech.name}-duplicate-${index}`}
                            className="flex items-center gap-2 text-muted-foreground/60 px-4"
                        >
                            <span className="flex-shrink-0">{tech.icon}</span>
                            <span className="text-xs font-medium tracking-wide">{tech.name}</span>
                        </div>
                    ))}
                </div>
            </div>
            <style jsx>{`
                .marquee-container {
                    display: flex;
                    width: 100%;
                    overflow: hidden;
                }
                .marquee-content {
                    display: flex;
                    flex-shrink: 0;
                    align-items: center;
                    gap: 0;
                    padding: 0.625rem 0;
                    animation: marquee 40s linear infinite;
                }
                @keyframes marquee {
                    0% {
                        transform: translateX(0);
                    }
                    100% {
                        transform: translateX(-100%);
                    }
                }
            `}</style>
        </div>
    )
}
